package gallery;

import javax.swing.*;

public class Image {
	
	private String name;
	private String path;
	
	public Image (String name, String path) {
		
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getPath() {
		return path;
	}
	
	public void setPath(String path) {
		this.path = path;
	}
	
	

}
